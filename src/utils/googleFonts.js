// Load Google Font dynamically
export const loadGoogleFont = (fontName) => {
  const font = GOOGLE_FONTS.find(f => f.name === fontName);
  if (!font) return;

  // Check if font is already loaded
  const existingLink = document.querySelector(`link[data-font="${fontName}"]`);
  if (existingLink) return;

  // Create link element
  const link = document.createElement('link');
  link.rel = 'stylesheet';
  link.href = `https://fonts.googleapis.com/css2?family=${fontName.replace(/ /g, '+')}:wght@300;400;500;600;700&display=swap`;
  link.setAttribute('data-font', fontName);
  document.head.appendChild(link);
};

// Load all Google Fonts at once (for admin panel)
export const loadAllGoogleFonts = () => {
  const fontFamilies = GOOGLE_FONTS
    .map(f => f.name.replace(/ /g, '+'))
    .join('&family=');
  
  const existingLink = document.querySelector('link[data-font="all-google-fonts"]');
  if (existingLink) return;

  const link = document.createElement('link');
  link.rel = 'stylesheet';
  link.href = `https://fonts.googleapis.com/css2?${fontFamilies.split('&family=').map(f => `family=${f}:wght@300;400;500;600;700`).join('&')}&display=swap`;
  link.setAttribute('data-font', 'all-google-fonts');
  document.head.appendChild(link);
};
